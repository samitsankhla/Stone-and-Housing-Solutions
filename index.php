            $phone = isset($_POST['phone']) ? mysqli_real_escape_string($conn, $_POST['phone']) : null;
            $message = mysqli_real_escape_string($conn, $_POST['message']);

            // Prepare INSERT statement
            $sql = "INSERT INTO contact_us (name, email, phone_no, message) VALUES (?, ?, ?, ?)";
            $stmt = mysqli_prepare($conn, $sql);

            // Bind parameters to the prepared statement
            mysqli_stmt_bind_param($stmt, "ssss", $name, $email, $phone, $message);

            // Execute the prepared statement
            if (mysqli_stmt_execute($stmt)) {
                echo "Data inserted successfully.";
            } else {
                echo "Error: " . mysqli_error($conn);
            }

            // Close statement
            mysqli_stmt_close($stmt);
        } else {
            echo "All required fields are not filled.";
        }
    }
